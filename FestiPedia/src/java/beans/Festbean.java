package beans;

/**
 *
 * @author Windmolders Jonas
 */
public class Festbean
{
    private String id, naam, locatie, datum, duur;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the naam
     */
    public String getNaam() {
        return naam;
    }

    /**
     * @param naam the naam to set
     */
    public void setNaam(String naam) {
        this.naam = naam;
    }

    /**
     * @return the locatie
     */
    public String getLocatie() {
        return locatie;
    }

    /**
     * @param locatie the locatie to set
     */
    public void setLocatie(String locatie) {
        this.locatie = locatie;
    }

    /**
     * @return the datum
     */
    public String getDatum() {
        return datum;
    }

    /**
     * @param datum the datum to set
     */
    public void setDatum(String datum) {
        this.datum = datum;
    }

    /**
     * @return the duur
     */
    public String getDuur() {
        return duur;
    }

    /**
     * @param duur the duur to set
     */
    public void setDuur(String duur) {
        this.duur = duur;
    }
 
    
   
}